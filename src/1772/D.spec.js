const { getStep } = require('./D');

describe('1772D. Absolute Sorting', () => {
  it.each`
    n    | array                                                        | result
    ${1} | ${[5, 3, 3, 3, 5]}                                           | ${4}
    ${2} | ${[5, 3, 4, 5]}                                              | ${-1}
    ${3} | ${[1, 2, 3, 4, 5, 6, 7, 8]}                                  | ${0}
    ${4} | ${[10, 5, 4, 3, 2, 1]}                                       | ${8}
    ${5} | ${[3, 3, 1]}                                                 | ${2}
    ${6} | ${[42, 43, 42]}                                              | ${-1}
    ${7} | ${[100000000, 99999999]}                                     | ${100000000}
    ${8} | ${[29613295, 52036613, 75100585, 78027446, 81409090, 73215]} | ${40741153}
  `('Base test: $n', ({ array, result }) => {
    expect(getStep(array)).toBe(result);
  });
});
