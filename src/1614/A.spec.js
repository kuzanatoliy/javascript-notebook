const { getCountOfChocolateBars } = require('./A');

describe('1614A. Divan and a Store', () => {
  it.each`
    n    | l             | r             | k             | array                                               | result
    ${1} | ${1}          | ${100}        | ${100}        | ${[50, 100, 50]}                                    | ${2}
    ${2} | ${3}          | ${5}          | ${10}         | ${[1, 2, 3, 4, 5, 6]}                               | ${2}
    ${3} | ${3}          | ${5}          | ${21}         | ${[1, 2, 3, 4, 5, 6]}                               | ${3}
    ${4} | ${50}         | ${69}         | ${100}        | ${[20, 30, 40, 77, 1, 1, 12, 4, 70, 10000]}         | ${0}
    ${5} | ${50}         | ${80}         | ${30}         | ${[20, 60, 70]}                                     | ${0}
    ${6} | ${2}          | ${7}          | ${100}        | ${[2, 2, 2, 2, 2, 7, 7, 7, 7, 7]}                   | ${10}
    ${7} | ${1000000000} | ${1000000000} | ${1000000000} | ${[1000000000, 1000000000, 1000000000, 1000000000]} | ${1}
    ${8} | ${1}          | ${1}          | ${1}          | ${[1]}                                              | ${1}
  `('Base test: $n', ({ l, r, k, array, result }) => {
    expect(getCountOfChocolateBars(l, r, k, array)).toBe(result);
  });
});
