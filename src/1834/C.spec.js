const { getMatrix } = require('./C');

describe('1834C. Game with Reversing', () => {
  it.each`
    n    | row  | col  | result
    ${1} | ${4} | ${4} | ${[[9, 10, 11, 12], [1, 2, 3, 4], [13, 14, 15, 16], [5, 6, 7, 8]]}
    ${2} | ${5} | ${7} | ${[[15, 16, 17, 18, 19, 20, 21], [1, 2, 3, 4, 5, 6, 7], [22, 23, 24, 25, 26, 27, 28], [8, 9, 10, 11, 12, 13, 14], [29, 30, 31, 32, 33, 34, 35]]}
    ${3} | ${6} | ${4} | ${[[13, 14, 15, 16], [1, 2, 3, 4], [17, 18, 19, 20], [5, 6, 7, 8], [21, 22, 23, 24], [9, 10, 11, 12]]}
  `('Base test: $n', ({ row, col, result }) => {
    expect(getMatrix(row, col)).toStrictEqual(result);
  });
});
