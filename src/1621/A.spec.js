const { getMap } = require('./A');

describe('1621A. Stable Arrangement of Rooks', () => {
  it.each`
    n    | num   | k     | result
    ${1} | ${3}  | ${2}  | ${[['R', '.', '.'], ['.', '.', '.'], ['.', '.', 'R']]}
    ${2} | ${3}  | ${3}  | ${-1}
    ${3} | ${1}  | ${1}  | ${[['R']]}
    ${4} | ${5}  | ${2}  | ${[['R', '.', '.', '.', '.'], ['.', '.', '.', '.', '.'], ['.', '.', 'R', '.', '.'], ['.', '.', '.', '.', '.'], ['.', '.', '.', '.', '.']]}
    ${5} | ${40} | ${33} | ${-1}
  `('Base test: $n', ({ num, k, result }) => {
    expect(getMap(num, k)).toStrictEqual(result);
  });
});
