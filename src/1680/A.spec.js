const { getMinLength } = require('./A');

describe('1680A. Minimums and Maximums', () => {
  it.each`
    n    | l1   | r1   | l2    | r2    | result
    ${1} | ${3} | ${5} | ${4}  | ${6}  | ${4}
    ${2} | ${5} | ${8} | ${5}  | ${5}  | ${5}
    ${3} | ${3} | ${3} | ${10} | ${12} | ${13}
    ${4} | ${1} | ${5} | ${3}  | ${3}  | ${3}
    ${5} | ${1} | ${1} | ${2}  | ${2}  | ${3}
    ${6} | ${2} | ${2} | ${1}  | ${1}  | ${3}
    ${7} | ${6} | ${6} | ${6}  | ${6}  | ${6}
  `('Base test: $n', ({ l1, r1, l2, r2, result }) => {
    expect(getMinLength(l1, r1, l2, r2)).toBe(result);
  });
});
