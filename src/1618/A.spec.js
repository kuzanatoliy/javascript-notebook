const { getInitNumbers } = require('./A');

describe('1618A. Polycarp and Sums of Subsequences', () => {
  it.each`
    n    | array                                                                            | result
    ${1} | ${[1, 3, 4, 4, 5, 7, 8]}                                                         | ${[1, 3, 4]}
    ${2} | ${[1, 2, 3, 4, 5, 6, 7]}                                                         | ${[1, 2, 4]}
    ${3} | ${[300000000, 300000000, 300000000, 600000000, 600000000, 600000000, 900000000]} | ${[300000000, 300000000, 300000000]}
    ${4} | ${[1, 1, 2, 999999998, 999999999, 999999999, 1000000000]}                        | ${[1, 1, 999999998]}
    ${5} | ${[1, 2, 2, 3, 3, 4, 5]}                                                         | ${[1, 2, 2]}
  `('Base test: $n', ({ array, result }) => {
    expect(getInitNumbers(array)).toStrictEqual(result);
  });
});
