const { getPlusOrMinus } = require('./A');

describe('1807A. Plus or Minus', () => {
  it.each`
    n     | a    | b    | c     | result
    ${1}  | ${1} | ${2} | ${3}  | ${'+'}
    ${2}  | ${3} | ${2} | ${1}  | ${'-'}
    ${3}  | ${2} | ${9} | ${-7} | ${'-'}
    ${4}  | ${3} | ${4} | ${7}  | ${'+'}
    ${5}  | ${1} | ${1} | ${2}  | ${'+'}
    ${6}  | ${1} | ${1} | ${0}  | ${'-'}
    ${7}  | ${3} | ${3} | ${6}  | ${'+'}
    ${8}  | ${9} | ${9} | ${18} | ${'+'}
    ${9}  | ${9} | ${9} | ${0}  | ${'-'}
    ${10} | ${1} | ${9} | ${-8} | ${'-'}
    ${11} | ${1} | ${9} | ${10} | ${'+'}
  `('Base test: $n', ({ a, b, c, result }) => {
    expect(getPlusOrMinus(a, b, c)).toBe(result);
  });
});
