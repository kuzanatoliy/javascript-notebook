const { getMaxMoney } = require('./B');

describe('1978B. New Bakery', () => {
  it.each`
    n    | num            | a              | b              | result
    ${1} | ${4n}          | ${4n}          | ${5n}          | ${17n}
    ${2} | ${5n}          | ${5n}          | ${9n}          | ${35n}
    ${3} | ${10n}         | ${10n}         | ${5n}          | ${100n}
    ${4} | ${5n}          | ${5n}          | ${11n}         | ${45n}
    ${5} | ${1000000000n} | ${1000000000n} | ${1000000000n} | ${1000000000000000000n}
    ${5} | ${1000000000n} | ${1000000000n} | ${1n}          | ${1000000000000000000n}
    ${5} | ${1000n}       | ${1n}          | ${1000n}       | ${500500n}
  `('Base test: $n', ({ num, a, b, result }) => {
    expect(getMaxMoney(num, a, b)).toBe(result);
  });
});
