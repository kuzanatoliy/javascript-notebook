const { getAmount } = require('./A');

describe('1440A. Buy the String', () => {
  it.each`
    n    | map               | c0     | c1     | h     | result
    ${1} | ${'100'}          | ${1}   | ${1}   | ${1}  | ${3}
    ${2} | ${'01010'}        | ${10}  | ${100} | ${1}  | ${52}
    ${3} | ${'11111'}        | ${10}  | ${1}   | ${1}  | ${5}
    ${4} | ${'11111'}        | ${1}   | ${10}  | ${1}  | ${10}
    ${5} | ${'101110110101'} | ${2}   | ${1}   | ${10} | ${16}
    ${6} | ${'00'}           | ${100} | ${1}   | ${10} | ${22}
  `('Base test: $n', ({ map, c0, c1, h, result }) => {
    expect(getAmount(map, c0, c1, h)).toBe(result);
  });
});
