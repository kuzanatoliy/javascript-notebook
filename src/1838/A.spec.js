const { getInnerNumber } = require('./A');

describe('1838A. Blackboard List', () => {
  it.each`
    n    | arr                                                             | result
    ${1} | ${[9, 2, 7]}                                                    | ${9}
    ${2} | ${[15, -4, 11]}                                                 | ${-4}
    ${3} | ${[-9, 1, 11, -10]}                                             | ${-10}
    ${4} | ${[3, 0, 0, 0, 3]}                                              | ${3}
    ${5} | ${[8, 16, 8, 0, 8, 16, 8]}                                      | ${16}
    ${6} | ${[0, 0, 0, 0]}                                                 | ${0}
    ${7} | ${[27, 1, 24, 28, 2, -1, 26, 25, 28, 27]}                       | ${-1}
    ${8} | ${[600000000, 800000000, 0, -200000000, 1000000000, 800000000]} | ${-200000000}
    ${9} | ${[0, -1000000000, 1000000000]}                                 | ${-1000000000}
  `('Base test: $n', ({ arr, result }) => {
    expect(getInnerNumber(arr)).toBe(result);
  });
});
