const { getMaxCountOfOperations } = require('./A');

describe('2025A. Set', () => {
  it.each`
    n    | l      | r             | k    | result
    ${1} | ${3}   | ${9}          | ${2} | ${2}
    ${2} | ${4}   | ${9}          | ${1} | ${6}
    ${3} | ${7}   | ${9}          | ${2} | ${0}
    ${4} | ${2}   | ${10}         | ${2} | ${4}
    ${5} | ${154} | ${220}        | ${2} | ${0}
    ${6} | ${147} | ${294}        | ${2} | ${1}
    ${7} | ${998} | ${24435}      | ${3} | ${7148}
    ${8} | ${1}   | ${1000000000} | ${2} | ${500000000}
  `('Base test: $n', ({ l, r, k, result }) => {
    expect(getMaxCountOfOperations(l, r, k)).toBe(result);
  });
});
