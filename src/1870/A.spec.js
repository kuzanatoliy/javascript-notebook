const { getMaxSum } = require('./A');

describe('1870A. MEXanized Array', () => {
  it.each`
    n     | num    | k     | x      | result
    ${1}  | ${5}   | ${3}  | ${3}   | ${7}
    ${2}  | ${4}   | ${7}  | ${5}   | ${-1}
    ${3}  | ${4}   | ${2}  | ${28}  | ${57}
    ${4}  | ${12}  | ${10} | ${6}   | ${-1}
    ${5}  | ${57}  | ${51} | ${122} | ${2007}
    ${6}  | ${200} | ${1}  | ${200} | ${39800}
    ${7}  | ${2}   | ${2}  | ${1}   | ${1}
    ${8}  | ${3}   | ${2}  | ${1}   | ${2}
    ${9}  | ${4}   | ${7}  | ${10}  | ${-1}
    ${10} | ${1}   | ${2}  | ${1}   | ${-1}
  `('Base test: $n', ({ num, k, x, result }) => {
    expect(getMaxSum(num, k, x)).toStrictEqual(result);
  });
});
