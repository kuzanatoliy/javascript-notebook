const { getSums } = require('./B');

describe('1744B. Even-Odd Increments', () => {
  it.each`
    n    | numbers                                             | queries                                                              | result
    ${1} | ${[1]}                                              | ${[[1, 1]]}                                                          | ${[2]}
    ${2} | ${[1, 2, 4]}                                        | ${[[0, 2], [1, 3], [0, 5]]}                                          | ${[11, 14, 29]}
    ${3} | ${[1, 3, 2, 4, 10, 48]}                             | ${[[1, 6], [0, 5], [0, 4], [0, 5], [1, 3], [0, 12], [0, 1]]}         | ${[80, 100, 100, 100, 118, 190, 196]}
    ${4} | ${[1000000000, 1000000000, 1000000000, 11, 15, 17]} | ${[[0, 17], [1, 10000], [1, 51], [0, 92], [0, 53], [1, 16], [0, 1]]} | ${[3000000094, 3000060094, 3000060400, 3000060952, 3000061270, 3000061366, 3000061366]}
  `('Base test: $n', ({ numbers, queries, result }) => {
    expect(getSums(numbers, queries)).toStrictEqual(result);
  });
});
