const { transformDate } = require('./C');

describe('1950C. Clock Conversion', () => {
  it.each`
    n     | date       | result
    ${1}  | ${'09:41'} | ${'09:41 AM'}
    ${2}  | ${'18:06'} | ${'06:06 PM'}
    ${3}  | ${'12:14'} | ${'12:14 PM'}
    ${4}  | ${'00:59'} | ${'12:59 AM'}
    ${5}  | ${'00:00'} | ${'12:00 AM'}
    ${6}  | ${'14:34'} | ${'02:34 PM'}
    ${7}  | ${'01:01'} | ${'01:01 AM'}
    ${8}  | ${'19:07'} | ${'07:07 PM'}
    ${9}  | ${'11:59'} | ${'11:59 AM'}
    ${10} | ${'12:00'} | ${'12:00 PM'}
    ${11} | ${'21:37'} | ${'09:37 PM'}
  `('Base test: $n', ({ date, result }) => {
    expect(transformDate(date)).toBe(result);
  });
});
