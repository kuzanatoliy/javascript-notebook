const { getDifs } = require('./A');

describe('1593A. Elections', () => {
  it.each`
    n    | a       | b             | c     | result
    ${1} | ${0}    | ${0}          | ${0}  | ${[1, 1, 1]}
    ${2} | ${10}   | ${75}         | ${15} | ${[66, 0, 61]}
    ${3} | ${13}   | ${13}         | ${17} | ${[5, 5, 0]}
    ${4} | ${1000} | ${0}          | ${0}  | ${[0, 1001, 1001]}
    ${5} | ${0}    | ${1000000000} | ${0}  | ${[1000000001, 0, 1000000001]}
  `('Base test: $n', ({ a, b, c, result }) => {
    expect(getDifs(a, b, c)).toStrictEqual(result);
  });
});
