const { getSteps } = require('./A');

describe('1816A. Ian Visits Mary', () => {
  it.each`
    n    | a             | b             | result
    ${1} | ${3}          | ${4}          | ${[2, [[2, 1], [3, 4]]]}
    ${2} | ${4}          | ${4}          | ${[2, [[3, 1], [4, 4]]]}
    ${3} | ${3}          | ${6}          | ${[2, [[2, 1], [3, 6]]]}
    ${4} | ${2}          | ${2}          | ${[2, [[1, 1], [2, 2]]]}
    ${5} | ${1}          | ${1}          | ${[2, [[0, 1], [1, 1]]]}
    ${6} | ${7}          | ${3}          | ${[2, [[6, 1], [7, 3]]]}
    ${7} | ${2022}       | ${2023}       | ${[2, [[2021, 1], [2022, 2023]]]}
    ${8} | ${1000000000} | ${1000000000} | ${[2, [[999999999, 1], [1000000000, 1000000000]]]}
  `('Base test: $n', ({ a, b, result }) => {
    expect(getSteps(a, b)).toStrictEqual(result);
  });
});
