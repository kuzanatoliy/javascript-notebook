const { transformMatrix } = require('./B');

describe('1986B. Matrix Stabilization', () => {
  it.each`
    n    | matrix                                                                                 | result
    ${1} | ${[[3, 1]]}                                                                            | ${[[1, 1]]}
    ${2} | ${[[1], [1]]}                                                                          | ${[[1], [1]]}
    ${3} | ${[[1, 2], [3, 4]]}                                                                    | ${[[1, 2], [3, 3]]}
    ${4} | ${[[7, 4, 5], [1, 8, 10]]}                                                             | ${[[4, 4, 5], [1, 8, 8]]}
    ${5} | ${[[92, 74, 31, 74], [74, 92, 17, 7], [31, 17, 92, 3], [74, 7, 3, 92], [7, 31, 1, 1]]} | ${[[74, 74, 31, 31], [74, 74, 17, 7], [31, 17, 17, 3], [31, 7, 3, 3], [7, 7, 1, 1]]}
    ${6} | ${[[1000000000, 1, 1000000000], [1, 1000000000, 1], [1000000000, 1, 1000000000]]}      | ${[[1, 1, 1], [1, 1, 1], [1, 1, 1]]}
  `('Base test: $n', ({ matrix, result }) => {
    expect(transformMatrix(matrix)).toStrictEqual(result);
  });
});
