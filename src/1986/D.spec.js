const { getMinResultOfExpression } = require('./D');

describe('1986D. Mathematical Problem', () => {
  it.each`
    n     | map                       | result
    ${1}  | ${'10'}                   | ${10}
    ${2}  | ${'74'}                   | ${74}
    ${3}  | ${'00'}                   | ${0}
    ${4}  | ${'01'}                   | ${1}
    ${5}  | ${'901'}                  | ${9}
    ${6}  | ${'101'}                  | ${1}
    ${7}  | ${'23311'}                | ${19}
    ${8}  | ${'987009'}               | ${0}
    ${9}  | ${'1111111'}              | ${11}
    ${10} | ${'99999999999999999999'} | ${261}
    ${11} | ${'00000000000000000000'} | ${0}
    ${12} | ${'0212'}                 | ${0}
    ${13} | ${'057235283621345395'}   | ${0}
    ${14} | ${'1112'}                 | ${12}
    ${15} | ${'19811678487321784121'} | ${93}
    ${16} | ${'1121'}                 | ${12}
    ${17} | ${'2221'}                 | ${24}
    ${18} | ${'011'}                  | ${0}
  `('Base test: $n', ({ map, result }) => {
    expect(getMinResultOfExpression(map)).toBe(result);
  });
});
