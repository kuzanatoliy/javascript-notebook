const { getCountOfAmount } = require('./C');

describe('1829C. Mr. Perfectly Fine', () => {
  it.each`
    n    | books                                                                             | result
    ${1} | ${[['2', '00'], ['3', '10'], ['4', '01'], ['4', '00']]}                           | ${7}
    ${2} | ${[['3', '01'], ['3', '01'], ['5', '01'], ['2', '10'], ['9', '10']]}              | ${5}
    ${3} | ${[['5', '11']]}                                                                  | ${5}
    ${4} | ${[['9', '11'], ['8', '01'], ['7', '10']]}                                        | ${9}
    ${5} | ${[['4', '01'], ['6', '01'], ['7', '01'], ['8', '00'], ['9', '01'], ['1', '00']]} | ${-1}
    ${6} | ${[['8', '00'], ['9', '10'], ['9', '11'], ['8', '11']]}                           | ${8}
  `('Base test: $n', ({ books, result }) => {
    expect(getCountOfAmount(books)).toBe(result);
  });
});
