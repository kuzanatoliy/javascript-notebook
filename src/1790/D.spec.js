const { getCountOfSets } = require('./D');

describe('1790D. Matryoshkas', () => {
  it.each`
    n     | array                                                                       | result
    ${1}  | ${[2, 2, 3, 4, 3, 1]}                                                       | ${2}
    ${2}  | ${[11, 8, 7, 10, 9]}                                                        | ${1}
    ${3}  | ${[1000000000, 1000000000, 1000000000, 1000000000, 1000000000, 1000000000]} | ${6}
    ${4}  | ${[1, 1, 4, 4, 2, 3, 2, 3]}                                                 | ${2}
    ${5}  | ${[1, 2, 3, 2, 3, 4]}                                                       | ${2}
    ${6}  | ${[10, 11, 11, 12, 12, 13, 13]}                                             | ${2}
    ${7}  | ${[8, 8, 9, 9, 10, 10, 11]}                                                 | ${2}
    ${8}  | ${[4, 14, 5, 15, 6, 16, 7, 17]}                                             | ${2}
    ${9}  | ${[5, 15, 6, 14, 8, 12, 9, 11]}                                             | ${4}
    ${10} | ${[4, 2, 2, 3, 4]}                                                          | ${3}
  `('Base test: $n', ({ array, result }) => {
    expect(getCountOfSets(array)).toBe(result);
  });
});
